@Injectable()
export class Web3Service {
  private readonly contract_addr = '0xBf028794F0C8437eD5e9263ad7aeE77D7E470bE3';
  constructor(@Inject('Web3') private readonly web3: Web3) {}

  /**
   * 잔액 조회
   * --
   * @param addr
   * @returns
   */
  async getBalance(addr: string) {
    try {
      const balance = await this.web3.eth.getBalance(addr);
      return this.web3.utils.fromWei(balance, 'ether');
    } catch (e) {
      throw e;
    }
  }

  /**
   * ERC20 전송
   * --
   * @param transferInfo
   * @returns
   */
  async transfer(transferInfo: TransferDTO) {
    try {
      const { to, amount } = transferInfo;
      const contract = new this.web3.eth.Contract(ERC20ABI, this.contract_addr);

      const data = contract.methods
        // @ts-ignore
        .transfer(to, amount)
        .encodeABI();
      const transferData: Transaction = {
        from: process.env.WEB3_ADDR,
        to: this.contract_addr,
        data,
        gasLimit: this.web3.utils.toHex(800000), // 적절한 가스 리밋 설정
        gasPrice: this.web3.utils.toHex(5000000000), // 적절한 가스 가격 설정
        chainId: 11155111,
      };
      const signedTx = await this.web3.eth.accounts.signTransaction(
        transferData,
        process.env.WEB3_PK,
      );
      await this.web3.eth.sendSignedTransaction(signedTx.rawTransaction);
      return true;
    } catch (e) {
      throw e;
    }
  }

  async getBlockNumber() {
    try {
      const blockNumber = await this.web3.eth.getBlockNumber();
      return blockNumber;
    } catch (e) {
      throw e;
    }
  }
}
